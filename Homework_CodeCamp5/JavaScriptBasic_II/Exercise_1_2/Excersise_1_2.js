//2. เขียนคำสั่ง if ที่เช็คอายุว่าอยู่ระหว่าง 18 - 60
let age = prompt("ช็คอายุว่าอยู่ระหว่าง 18 - 60 ปี หรือไม่ \n กรอกอายุ :");

if (!Boolean(Number(age)) && String(age) === String(''))
    alert("กรอกข้อมูลไม่ถูกต้อง");
else if (Number(age) > 18 && Number(age) < 60)
    alert("ใช่ อายุอยู่ระหว่าง 18 - 60 ปี");
else
    alert("ไม่ใช่ อายุไม่ได้อยู่ระหว่าง 18 - 60 ปี");